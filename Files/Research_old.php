		<td class="body1">
		As a Ph.D. student in the <a href="http://www.amsc.umd.edu/">Applied Mathematics & Statistics, and Scientific Computation</a> program at the University of Maryland, College Park I completed the <a href="http://www.amsc.umd.edu/programs/sc_curriculum.html">Scientific Computation</a> concentration, and defended my thesis in 2012.  
		My research is in the field of <a href="http://en.wikipedia.org/wiki/Computer_vision">Computer Vision</a> under the supervision of <a href="http://www.cs.umd.edu/~djacobs/">Dr. David Jacobs</a>, Professor of Computer Science.  
		I am interested in methods of shape representation as a subfield of object recognition.
		My Ph.D. explored deformation and illumination insensitive object recognition algorithms based on measuring dense 2-D morphings between deformable objects such as faces.
		<br><br>

		I was recently been employed as an intern at the <a href="http://www.jhuapl.edu/">Johns Hopkins Applied Physics Lab</a>, 
		where I was researching methods of estimating the pose of a known object which is seen by several cameras in a low-bandwidth sensor network, 
		and was developing a dynamic model of the heart to aid in surgical planning.  Previously, I spent several summers writing surface optimization software with the Math and Computing Technology Research Group at <a href="http://www.boeing.com/">The Boeing Company</a>.
		<br><br>

		From January to July 2010 I was a visiting graduate researcher at the <a href="http://www.cmla.ens-cachan.fr/">Center for Mathematical Studies and their Applications</a> at the <a href="http://www.ens-cachan.fr/version-anglaise/">Ecole Normale Sup&eacute;rieure de Cachan</a> near Paris, France, studying deformable shape metrics under the direction of Professor <a href="http://atrouve.perso.math.cnrs.fr/">Alain Trouv&eacute;</a>.
		<br><br>

		Since defending my thesis, I have joined <a href="http://cvlab.epfl.ch/~fua/">Pascal Fua</a>'s Computer Vision Lab at the <a href="http://information.epfl.ch/glance">Ecole Polytechnique F&eacute;d&eacute;rale de Lausanne (EPFL)</a> in Lausanne, Switzerland as a postdoctoral researcher.


